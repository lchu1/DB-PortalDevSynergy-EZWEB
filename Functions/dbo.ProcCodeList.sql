CREATE FUNCTION [dbo].[ProcCodeList]( @AUTHNO VARCHAR(25))

RETURNS VARCHAR(500)
AS
BEGIN
DECLARE @ProcCodeList VARCHAR(500)

SELECT @ProcCodeList = COALESCE(@ProcCodeList + ', ', '') + 
   CAST(LTRIM(RTRIM(ProcCode)) AS VARCHAR(5)) 
FROM [EZCAP65TEST].[EZCAPDB].dbo.CHCNPortal_AuthDetails_VS
WHERE AUTHNO = @AUTHNO

RETURN @ProcCodeList

END





GO
