CREATE FUNCTION [dbo].[DiagCodeList]( @AUTHNO VARCHAR(25))

RETURNS VARCHAR(500)
AS
BEGIN
DECLARE @DiagCodeList VARCHAR(500)

SELECT @DiagCodeList = COALESCE(@DiagCodeList + ', ', '') + 
   CAST(LTRIM(RTRIM(DiagCode)) AS VARCHAR(5)) 
FROM [EZCAP65TEST].[EZCAPDB].dbo.CHCNPortal_AuthDetails_VS
WHERE AUTHNO = @AUTHNO

RETURN @DiagCodeList

END




GO
