CREATE           PROCEDURE [dbo].[CHCNWEB_GetCHCs]

@COMPANYID varchar(20)

as

SET @COMPANYID = REPLACE(@COMPANYID,'-','') 

SELECT DISTINCT     NAME, VENDORID
FROM         CHCNWEB_CHCN_CLINICS
WHERE REPLACE(VENDORID,'-','') = @COMPANYID
ORDER BY NAME ASC

















GO
