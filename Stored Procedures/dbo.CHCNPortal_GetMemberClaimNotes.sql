CREATE PROCEDURE [dbo].[CHCNPortal_GetMemberClaimNotes]

@CLAIMNO varchar(25)

AS

If @CLAIMNO IS NULL 
	BEGIN
	RETURN
	END
ELSE
BEGIN

	SELECT   Subject, Notes
	FROM   CHCNPORTAL_CLAIM_NOTES -- EZCAPDB2.EZCAPDB.dbo.CLAIM_NOTES_VS
	WHERE     (CLAIMNO = @CLAIMNO)

END


GO
