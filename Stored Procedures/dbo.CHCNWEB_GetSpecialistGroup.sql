CREATE      PROCEDURE [dbo].[CHCNWEB_GetSpecialistGroup]

@CODE varchar (10)

as

SELECT DISTINCT CODE, DESCR, MEDICAL_GROUP, MED_GROUP = CASE WHEN CITY IS NULL THEN MEDICAL_GROUP ELSE MEDICAL_GROUP + ',' + ' ' + CITY END 
FROM         CHCNWEB_SPECIALISTS
WHERE CODE = @CODE
ORDER BY DESCR, MEDICAL_GROUP















GO
