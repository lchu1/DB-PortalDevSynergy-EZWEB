CREATE PROCEDURE [dbo].[CHCNAuthReq_DeleteAuthICDTemp]

@ID VARCHAR(20)

AS

SET @ID = LTRIM(RTRIM(REPLACE(REPLACE(REPLACE(@ID,CHAR(9),''),CHAR(10),''),CHAR(13),'')))

DELETE 
FROM CHCNAuthReq_ICD_Temp
WHERE ID = @ID

IF (SELECT count(*) FROM CHCNAuthReq_ICD_Temp WHERE ID=@ID) = 0
    BEGIN
    RETURN 1
    END
ELSE
    BEGIN
    RETURN -1
    END





GO
