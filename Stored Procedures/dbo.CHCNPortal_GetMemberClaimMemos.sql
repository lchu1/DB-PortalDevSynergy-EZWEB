CREATE PROCEDURE [dbo].[CHCNPortal_GetMemberClaimMemos]

-- UPDATED TO USE EZWEB.DBO.CHCNPORTAL_CLAIM_MEMOS, REFRESHED NIGHTLY, CTA, 01/22/2014

@CLAIMNO varchar(25)

AS

If @CLAIMNO IS NULL 
	BEGIN
	RETURN
	END
ELSE
BEGIN

	SELECT Memoline1,Memoline2,Memoline3,Memoline4
	FROM    dbo.CHCNPORTAL_CLAIM_MEMOS
	WHERE     (CLAIMNO = @CLAIMNO)

END


GO
